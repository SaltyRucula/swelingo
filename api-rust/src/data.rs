/// Load challenges from JSON files in the `challenges/` directory (relative to
/// the working directory), then merge with the hardcoded set.  JSON files win
/// on duplicate IDs so new files can override old entries.
fn load_json_challenges() -> Vec<Challenge> {
    let dir = std::path::Path::new("challenges");
    if !dir.is_dir() {
        return vec![];
    }
    let mut entries: Vec<Challenge> = Vec::new();
    if let Ok(read) = std::fs::read_dir(dir) {
        for entry in read.flatten() {
            let path = entry.path();
            if path.extension().and_then(|e| e.to_str()) != Some("json") {
                continue;
            }
            if let Ok(contents) = std::fs::read_to_string(&path) {
                match serde_json::from_str::<Vec<Challenge>>(&contents) {
                    Ok(challenges) => entries.extend(challenges),
                    Err(e) => eprintln!("Warning: failed to parse {:?}: {}", path, e),
                }
            }
        }
    }
    entries
}

