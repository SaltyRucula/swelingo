pub fn challenges() -> Vec<Challenge> {
    let json_challenges = load_json_challenges();
    let json_ids: std::collections::HashSet<String> =
        json_challenges.iter().map(|c| c.id.clone()).collect();

    let mut all = json_challenges;
    // Append hardcoded challenges whose IDs aren't already covered by JSON files
    all.extend(
        hardcoded_challenges()
            .into_iter()
            .filter(|c| !json_ids.contains(&c.id)),
    );
    all
}

